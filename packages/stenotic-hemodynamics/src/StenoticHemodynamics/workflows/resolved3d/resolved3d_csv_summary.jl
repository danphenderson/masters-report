"""
    write_comparison_summary_csv(path, rows; overwrite=false)

Write the aggregated per-case CSV summary for one resolved-3D comparison run.
These rows condense the imported resolved-3D versus 1D discrepancies and solver
health metrics.
"""
function write_comparison_summary_csv(
    path::String,
    rows::Vector{ComparisonSummaryRow};
    overwrite::Bool = false,
)
    write_csv_table(path, comparison_summary_header(), (comparison_summary_values(row) for row in rows); overwrite=overwrite)
end

